#!/usr/bin/env node
// ============================================================================
// Build script — Synergy seed
// ============================================================================
// Lê os guias em ../../estrategia/synergy/guias/*.md e gera
// src/data/synergy-seed.ts com os 17 registos prontos a inserir na tabela
// catalog_activities. Corre localmente (dev) antes de commit — Vercel só usa o
// ficheiro TS gerado.
//
// Uso:  cd backoffice && node scripts/build-synergy-seed.mjs
// ============================================================================

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, basename } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const GUIAS = join(HERE, "..", "..", "estrategia", "synergy", "guias");
const OUT = join(HERE, "..", "src", "data", "synergy-seed.ts");

const FAMILY_ORDER = ["wild", "hive", "multi"];

// -- Field extractors -------------------------------------------------------

function extractH1(md) {
  const m = md.match(/^#\s+(.+)$/m);
  return m ? m[1].trim() : "";
}

function extractTagline(md) {
  // First "> text" blockquote right after the H1
  const m = md.match(/^>\s+(.+)$/m);
  return m ? m[1].trim() : null;
}

function extractField(md, label) {
  // Matches:  **Duração:** meio-dia ...
  const re = new RegExp(`\\*\\*${label}:\\*\\*\\s*([^\\n]+)`, "i");
  const m = md.match(re);
  return m ? m[1].trim() : null;
}

// Parse "10–40 (equipas de 5-6)"  ->  { min: 10, max: 40 }
function parseRange(str) {
  if (!str) return { min: null, max: null };
  const cleaned = str
    .replace(/[–—]/g, "-")
    .replace(/\bpax\b/gi, "")
    .trim();
  const m = cleaned.match(/(\d+)\s*-\s*(\d+)/);
  if (m) return { min: parseInt(m[1], 10), max: parseInt(m[2], 10) };
  const single = cleaned.match(/(\d+)\s*[+]?/);
  if (single) return { min: parseInt(single[1], 10), max: null };
  return { min: null, max: null };
}

// Parse "~35-40€/pax"  ->  { min: 35, max: 40 }
function parsePriceRange(md) {
  const re = /Preço-alvo:\s*\*\*[~]?(\d+)[-–—](\d+)€\/pax\*\*/i;
  const m = md.match(re);
  if (m) return { min: parseInt(m[1], 10), max: parseInt(m[2], 10) };
  const single = /Preço-alvo:\s*\*\*[~]?(\d+)€\/pax\*\*/i.exec(md);
  if (single) return { min: parseInt(single[1], 10), max: parseInt(single[1], 10) };
  return { min: null, max: null };
}

// Very rough: look for "custo total X" or "custo material ~ Y" and estimate a
// per-event target price. We keep null and let the operator fill in via CRUD.
function extractPriceTarget(md) {
  const m = md.match(/Preço[- ]alvo:\s*\*\*[~]?(\d+)[-–—](\d+)€/i);
  if (m) return { min: parseInt(m[1], 10), max: parseInt(m[2], 10) };
  return { min: null, max: null };
}

function extractCode(fileName) {
  // "wild-01-treasure-hunters.md" -> "wild-01-treasure-hunters"
  return basename(fileName, ".md");
}

function extractFamily(code) {
  const prefix = code.split("-")[0];
  return FAMILY_ORDER.includes(prefix) ? prefix : "wild";
}

function extractSortOrder(code) {
  const m = code.match(/^\w+-(\d+)/);
  return m ? parseInt(m[1], 10) : 0;
}

function stripEmojiPrefix(name) {
  return name
    .replace(/^[\p{Extended_Pictographic}\s]+/u, "")
    .trim();
}

// -- Build ------------------------------------------------------------------

const files = readdirSync(GUIAS)
  .filter((f) => f.endsWith(".md"))
  .sort();

const activities = files.map((file) => {
  const code = extractCode(file);
  const md = readFileSync(join(GUIAS, file), "utf8");
  const name = extractH1(md);
  const cleanName = stripEmojiPrefix(name);
  const tagline = extractTagline(md);
  const family = extractFamily(code);
  const sortOrder = extractSortOrder(code);
  const duration = extractField(md, "Duração");
  const pax = extractField(md, "Participantes");
  const audience = extractField(md, "Público-alvo");
  const paxRange = parseRange(pax);
  const priceRange = parsePriceRange(md);
  const targetRange = extractPriceTarget(md);
  return {
    code,
    name: name || cleanName,
    tagline,
    family,
    durationLabel: duration,
    paxMin: paxRange.min,
    paxMax: paxRange.max,
    pricePerPaxMin: priceRange.min,
    pricePerPaxMax: priceRange.max,
    priceTargetMin: targetRange.min,
    priceTargetMax: targetRange.max,
    targetAudience: audience,
    sortOrder,
    body: md,
  };
});

function esc(s) {
  if (s == null) return "null";
  return "`" + String(s).replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${") + "`";
}

function numOrNull(n) {
  return n == null ? "null" : String(n);
}

const header = `// AUTO-GENERATED by scripts/build-synergy-seed.mjs — DO NOT EDIT BY HAND.
// Re-run with:  node scripts/build-synergy-seed.mjs
//
// Source: ../../estrategia/synergy/guias/*.md
// Generated: ${new Date().toISOString().slice(0, 10)}

export type SynergySeedActivity = {
  code: string;
  name: string;
  tagline: string | null;
  family: "wild" | "hive" | "multi";
  durationLabel: string | null;
  paxMin: number | null;
  paxMax: number | null;
  pricePerPaxMin: number | null;
  pricePerPaxMax: number | null;
  priceTargetMin: number | null;
  priceTargetMax: number | null;
  targetAudience: string | null;
  sortOrder: number;
  body: string;
};

export const SYNERGY_SEED: SynergySeedActivity[] = [
`;

const items = activities
  .map(
    (a) => `  {
    code: ${esc(a.code)},
    name: ${esc(a.name)},
    tagline: ${esc(a.tagline)},
    family: "${a.family}",
    durationLabel: ${esc(a.durationLabel)},
    paxMin: ${numOrNull(a.paxMin)},
    paxMax: ${numOrNull(a.paxMax)},
    pricePerPaxMin: ${numOrNull(a.pricePerPaxMin)},
    pricePerPaxMax: ${numOrNull(a.pricePerPaxMax)},
    priceTargetMin: ${numOrNull(a.priceTargetMin)},
    priceTargetMax: ${numOrNull(a.priceTargetMax)},
    targetAudience: ${esc(a.targetAudience)},
    sortOrder: ${a.sortOrder},
    body: ${esc(a.body)},
  },`,
  )
  .join("\n");

const footer = `
];
`;

writeFileSync(OUT, header + items + footer, "utf8");
console.log(`✓ Wrote ${OUT}`);
console.log(`  ${activities.length} activities.`);
